SELECT PLACE, DISTRICT,(G0_4+G5_9+G10_14+G15_19+G20_24+G25_29+G30_34)*100/TOTAL AS PERCENT_0_35 FROM census2011
ORDER BY PERCENT_0_35 DESC;

SELECT DISTRICT,SUM(G0_4+G5_9+G10_14+G15_19+G20_24+G25_29+G30_34)*100/SUM(TOTAL) AS PERCENT_0_35 FROM census2011
GROUP BY DISTRICT
ORDER BY PERCENT_0_35 DESC;

